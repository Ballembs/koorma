// ANALYZE LETTER — mask + zones + guide path
// ═══════════════════════════════════════════════

function analyzeLetter(vowel, S = 300) {
  const { letter, strokes: manualStrokes } = vowel;

  // Create mask for validation (40-cell grid)
  const c = document.createElement("canvas"); c.width = S; c.height = S;
  const ctx = c.getContext("2d");
  const fs = Math.round(S * 0.65);
  ctx.fillStyle = "black";
  ctx.font = `800 ${fs}px "Noto Sans Telugu", sans-serif`;
  ctx.textAlign = "center"; ctx.textBaseline = "middle";
  ctx.fillText(letter, S / 2, S / 2);

  const px = ctx.getImageData(0, 0, S, S).data;
  const G = 40, cs = S / G;
  const grid = Array.from({ length: G }, () => Array(G).fill(false));
  let x1 = S, y1 = S, x2 = 0, y2 = 0, fc = 0;
  for (let y = 0; y < S; y++)
    for (let x = 0; x < S; x++)
      if (px[(y * S + x) * 4 + 3] > 60) {
        const col = Math.min(Math.floor(x / cs), G - 1);
        const row = Math.min(Math.floor(y / cs), G - 1);
        if (!grid[row][col]) { grid[row][col] = true; fc++; }
        x1 = Math.min(x1, x); y1 = Math.min(y1, y);
        x2 = Math.max(x2, x); y2 = Math.max(y2, y);
      }

  const bw = x2 - x1, bh = y2 - y1;

  // 7×7 zones
  const ZR = 7, ZC = 7, zones = [];
  for (let zr = 0; zr < ZR; zr++)
    for (let zc = 0; zc < ZC; zc++) {
      const zx1 = x1 + (zc / ZC) * bw, zx2 = x1 + ((zc + 1) / ZC) * bw;
      const zy1 = y1 + (zr / ZR) * bh, zy2 = y1 + ((zr + 1) / ZR) * bh;
      let has = false;
      for (let r = Math.floor(zy1 / cs); r <= Math.min(Math.floor(zy2 / cs), G - 1) && !has; r++)
        for (let cc = Math.floor(zx1 / cs); cc <= Math.min(Math.floor(zx2 / cs), G - 1) && !has; cc++)
          if (grid[r]?.[cc]) has = true;
      zones.push({ zr, zc, has, x1: zx1, y1: zy1, x2: zx2, y2: zy2 });
    }
  const activeZones = zones.filter(z => z.has);

  // Guide path: use manual strokes if available, else auto-extract
  let guideStrokes; // array of strokes, each stroke = [{x,y}...] in canvas pixels
  if (manualStrokes && manualStrokes.length > 0) {
    // Manual: convert normalized coords to canvas pixels
    guideStrokes = manualStrokes.map(stroke =>
      stroke.map(p => ({ x: x1 + p.x * bw, y: y1 + p.y * bh }))
    );
  } else {
    // Auto-extract centerline
    const auto = autoExtractCenterline(letter, S);
    guideStrokes = [auto.path.map(p => ({
      x: x1 + p.x * bw,
      y: y1 + p.y * bh,
    }))];
  }

  return {
    grid, G, S, cs, fs, fc,
    bounds: { x1, y1, x2, y2 }, bw, bh,
    zones, activeZones,
    guideStrokes,
  };
}

// ═══════════════════════════════════════════════
// VALIDATION (zone-based from V6)
// ═══════════════════════════════════════════════

function validate(pts, mask, opts = {}) {
  const { minOn = 0.50, minZ = 0.70, minBB = 0.55, zPad = 5, minPts = 25 } = opts;
  if (pts.length < minPts)
    return { pass: false, on: 0, zc: 0, bb: 0, fb: "Draw more! Trace the whole letter 🐢" };
  const { grid, G, cs, bw, bh, activeZones: az } = mask;
  let ux1 = 1e9, uy1 = 1e9, ux2 = 0, uy2 = 0;
  for (const p of pts) { ux1 = Math.min(ux1, p.x); uy1 = Math.min(uy1, p.y); ux2 = Math.max(ux2, p.x); uy2 = Math.max(uy2, p.y); }
  const bb = Math.min(bw > 0 ? (ux2 - ux1) / bw : 0, bh > 0 ? (uy2 - uy1) / bh : 0);
  let onC = 0;
  for (const p of pts) {
    const col = Math.floor(p.x / cs), row = Math.floor(p.y / cs);
    let hit = false;
    for (let dr = -1; dr <= 1 && !hit; dr++)
      for (let dc = -1; dc <= 1 && !hit; dc++) {
        const r = row + dr, c = col + dc;
        if (r >= 0 && r < G && c >= 0 && c < G && grid[r][c]) hit = true;
      }
    if (hit) onC++;
  }
  const on = onC / pts.length;
  const vz = new Set();
  for (const p of pts)
    for (const z of az)
      if (p.x >= z.x1 - zPad && p.x <= z.x2 + zPad && p.y >= z.y1 - zPad && p.y <= z.y2 + zPad)
        vz.add(`${z.zr},${z.zc}`);
  const zc = az.length > 0 ? vz.size / az.length : 1;
  const pass = on >= minOn && zc >= minZ && bb >= minBB;
  let fb;
  if (pass) fb = zc > 0.85 && on > 0.65 ? "Wonderful! ✨" : "Good job! 🐢";
  else if (bb < minBB) fb = "Make it bigger! Trace the WHOLE letter 🐢";
  else if (on < minOn) fb = "Stay ON the letter shape!";
  else fb = `You missed some parts! (${az.length - vz.size} sections) 🐢`;
  return { pass, on, zc, bb, fb, missed: az.length - vz.size };
}

