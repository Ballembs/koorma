function WriteStep({ vowel, onComplete, size }) {
  const ref = useRef(null), maskRef = useRef(null);
  const allPts = useRef([]), curPts = useRef([]);
  const [drawing, setDrawing] = useState(false);
  const [hasDrawn, setHasDrawn] = useState(false);
  const [state, setState] = useState("ready");
  const [attempts, setAttempts] = useState(0);
  const [hint, setHint] = useState(false);
  const [result, setResult] = useState(null);
  const S = size ?? 300;

  useEffect(() => {
    const t = setTimeout(() => { maskRef.current = analyzeLetter(vowel, S); iCv(ref, S); }, 200);
    return () => clearTimeout(t);
  }, [vowel]);

  const showHint = useCallback(() => {
    if (!ref.current || !maskRef.current) return;
    const ctx = rCtx(ref, S); if (!ctx) return;
    ctx.font = `800 ${maskRef.current.fs}px "Noto Sans Telugu", sans-serif`;
    ctx.textAlign = "center"; ctx.textBaseline = "middle";
    ctx.fillStyle = "rgba(190,175,155,0.12)";
    ctx.fillText(vowel.letter, S / 2, S / 2);
  }, [vowel]);
  useEffect(() => { if (hint) showHint(); }, [hint, showHint]);

  const pos = useCallback((e) => cPos(e, ref, S), []);
  const onStart = useCallback((e) => {
    e.preventDefault(); if (state === "success") return;
    setDrawing(true); setHasDrawn(true);
    if (state !== "writing") setState("writing");
    const p = pos(e); curPts.current = [p];
    const ctx = rCtx(ref, S);
    if (ctx) { ink(ctx, C.kolam); ctx.beginPath(); ctx.moveTo(p.x, p.y); }
  }, [pos, state]);
  const onMove = useCallback((e) => {
    e.preventDefault(); if (!drawing) return;
    const p = pos(e); curPts.current.push(p);
    const ctx = rCtx(ref, S);
    if (ctx) { ink(ctx, C.kolam); ctx.lineTo(p.x, p.y); ctx.stroke(); ctx.beginPath(); ctx.moveTo(p.x, p.y); }
  }, [drawing, pos]);
  const onEnd = useCallback(() => {
    if (!drawing) return; setDrawing(false);
    allPts.current = [...allPts.current, ...curPts.current]; curPts.current = [];
  }, [drawing]);
  const check = () => {
    if (!maskRef.current) return;
    const r = validate(allPts.current, maskRef.current, { minOn: 0.40, minZ: 0.60, minBB: 0.50, zPad: 8 });
    setResult(r);
    const n = attempts + 1; setAttempts(n);
    if (r.pass) setState("success"); else { setState("failed"); if (n >= 2) setHint(true); }
  };
  const doClear = () => {
    clr(ref); allPts.current = []; curPts.current = [];
    setHasDrawn(false); setState("ready"); setResult(null);
    if (hint) showHint();
  };

  return (
    <Col>
      <Hdr title="Write from Memory 🌟" sub={<>Write <Te>{vowel.letter}</Te> ({vowel.trans}) — no guide!</>} />
      <div style={{
        alignSelf: "flex-end", marginRight: 10, background: "#F5F0EB",
        border: "1px solid #E0D5C8", borderRadius: 8, padding: "3px 10px",
        display: "flex", alignItems: "center", gap: 5
      }}>
        <span style={{ fontFamily: "'Noto Sans Telugu', sans-serif", fontWeight: 700, fontSize: 18, color: C.darkMuted }}>{vowel.letter}</span>
        <span style={{ fontSize: 11, color: C.darkMuted, fontFamily: "'Nunito', sans-serif" }}>({vowel.trans})</span>
      </div>
      <Box size={S} state={state}>
        <canvas ref={ref} style={{ position: "absolute", inset: 0, touchAction: "none", cursor: "crosshair" }}
          onMouseDown={onStart} onMouseMove={onMove} onMouseUp={onEnd} onMouseLeave={onEnd}
          onTouchStart={onStart} onTouchMove={onMove} onTouchEnd={onEnd} />
        {state === "ready" && !hasDrawn && (
          <div style={{ position: "absolute", inset: 0, display: "flex", alignItems: "center", justifyContent: "center", pointerEvents: "none" }}>
            <span style={{ ...pill, background: "rgba(255,255,255,0.8)" }}>
              ✏️ {hint ? "Follow the faint hint!" : "Write from memory!"}
            </span>
          </div>
        )}
      </Box>
      {state === "success" && <FB icon="🌟" text={`⭐⭐⭐ You can write ${vowel.letter}!`} color={C.mango}
        detail="అద్భుతం! (Wonderful!)" />}
      {state === "failed" && result && <FB icon="🐢" text={result.fb} color={C.terra}
        detail={hint ? "⭐ Follow the hint and try again!" : "⭐ Almost! Try once more!"} />}
      <BtnRow>
        {state === "success" ? <Btn onClick={onComplete} bg={C.turmeric} full>🎉 Complete!</Btn>
          : state === "failed" ? <>
            <BtnO onClick={doClear} full>🔄 Try again</BtnO>
            {attempts >= 4 && <Btn onClick={onComplete} bg={C.darkMuted} full>Continue →</Btn>}
          </> : <>
            <BtnO onClick={doClear} full>🔄 Clear</BtnO>
            <Btn onClick={check} bg={C.turmeric} full disabled={!hasDrawn}>Check ✓</Btn>
          </>}
      </BtnRow>
    </Col>
  );
}

