// ═══════════════════════════════════════════════
// AUTO CENTERLINE EXTRACTION
//
// For letters without hand-calibrated strokes:
// 1. Render letter filled at high res
// 2. Compute distance transform (distance of each
//    filled pixel to nearest empty pixel)
// 3. Extract ridge pixels (local maxima of distance)
//    = medial axis / skeleton
// 4. Order ridge pixels into a drawable path using
//    nearest-neighbor chain
// 5. Smooth the path
// ═══════════════════════════════════════════════

function autoExtractCenterline(letter, S = 300) {
  const c = document.createElement("canvas");
  c.width = S; c.height = S;
  const ctx = c.getContext("2d");
  const fs = Math.round(S * 0.65);

  ctx.fillStyle = "black";
  ctx.font = `800 ${fs}px "Noto Sans Telugu", sans-serif`;
  ctx.textAlign = "center"; ctx.textBaseline = "middle";
  ctx.fillText(letter, S / 2, S / 2);

  const imgData = ctx.getImageData(0, 0, S, S);
  const px = imgData.data;

  // Build binary grid (downsampled to ~2px cells for speed)
  const G = 150; // grid resolution
  const cell = S / G;
  const filled = Array.from({ length: G }, () => Array(G).fill(false));
  let x1 = S, y1 = S, x2 = 0, y2 = 0;

  for (let y = 0; y < S; y++) {
    for (let x = 0; x < S; x++) {
      if (px[(y * S + x) * 4 + 3] > 60) {
        const r = Math.min(Math.floor(y / cell), G - 1);
        const cc = Math.min(Math.floor(x / cell), G - 1);
        filled[r][cc] = true;
        x1 = Math.min(x1, x); y1 = Math.min(y1, y);
        x2 = Math.max(x2, x); y2 = Math.max(y2, y);
      }
    }
  }

  // Distance transform (approximate via BFS from boundary)
  const dist = Array.from({ length: G }, () => Array(G).fill(0));
  const queue = [];

  // Seed: all filled cells adjacent to empty cells
  for (let r = 0; r < G; r++) {
    for (let cc = 0; cc < G; cc++) {
      if (!filled[r][cc]) continue;
      let isBorder = false;
      for (let dr = -1; dr <= 1 && !isBorder; dr++)
        for (let dc = -1; dc <= 1 && !isBorder; dc++) {
          const nr = r + dr, nc = cc + dc;
          if (nr < 0 || nr >= G || nc < 0 || nc >= G || !filled[nr][nc])
            isBorder = true;
        }
      if (isBorder) {
        dist[r][cc] = 1;
        queue.push([r, cc]);
      }
    }
  }

  // BFS inward
  let qi = 0;
  while (qi < queue.length) {
    const [r, cc] = queue[qi++];
    for (let dr = -1; dr <= 1; dr++) {
      for (let dc = -1; dc <= 1; dc++) {
        if (dr === 0 && dc === 0) continue;
        const nr = r + dr, nc = cc + dc;
        if (nr >= 0 && nr < G && nc >= 0 && nc < G && filled[nr][nc] && dist[nr][nc] === 0) {
          dist[nr][nc] = dist[r][cc] + 1;
          queue.push([nr, nc]);
        }
      }
    }
  }

  // Extract ridge: cells that are local maxima of distance
  const ridge = [];
  for (let r = 1; r < G - 1; r++) {
    for (let cc = 1; cc < G - 1; cc++) {
      if (!filled[r][cc] || dist[r][cc] < 2) continue;
      const d = dist[r][cc];
      // Check if it's a local max (>= all 8-neighbors)
      let isMax = true;
      let higherCount = 0;
      for (let dr = -1; dr <= 1 && isMax; dr++)
        for (let dc = -1; dc <= 1; dc++) {
          if (dr === 0 && dc === 0) continue;
          if (dist[r + dr][cc + dc] > d) higherCount++;
        }
      // Ridge: local max OR near-max (at most 1 higher neighbor)
      if (higherCount <= 1) {
        ridge.push({ r, c: cc, d });
      }
    }
  }

  if (ridge.length < 5) {
    // Fallback: just use the skeleton erosion approach
    return fallbackSkeleton(filled, G, cell, x1, y1, x2, y2, S);
  }

  // Order ridge pixels into a path via nearest-neighbor
  const path = orderPixels(ridge, G, cell);

  // Smooth
  const smoothed = smoothPath(path, 4);

  // Convert to normalized coords relative to letter bbox
  const bw = x2 - x1, bh = y2 - y1;
  const normalized = smoothed.map(p => ({
    x: bw > 0 ? (p.x - x1) / bw : 0.5,
    y: bh > 0 ? (p.y - y1) / bh : 0.5,
  }));

  return { path: normalized, bounds: { x1, y1, x2, y2, w: bw, h: bh } };
}

function fallbackSkeleton(filled, G, cell, x1, y1, x2, y2, S) {
  // Iterative erosion
  let skel = filled.map(r => [...r]);
  let count = 0;
  for (let r = 0; r < G; r++) for (let c = 0; c < G; c++) if (skel[r][c]) count++;
  const target = Math.max(Math.floor(count * 0.12), 8);

  for (let iter = 0; iter < 100 && count > target; iter++) {
    const rm = [];
    for (let r = 0; r < G; r++)
      for (let c = 0; c < G; c++) {
        if (!skel[r][c]) continue;
        const n = (r > 0 && skel[r - 1][c]) ? 1 : 0;
        const s = (r < G - 1 && skel[r + 1][c]) ? 1 : 0;
        const e = (c < G - 1 && skel[r][c + 1]) ? 1 : 0;
        const w = (c > 0 && skel[r][c - 1]) ? 1 : 0;
        if (n + s + e + w < 4 && n + s + e + w >= 2) rm.push([r, c]);
      }
    if (rm.length === 0) break;
    for (const [r, c] of rm) { skel[r][c] = false; count--; if (count <= target) break; }
  }

  const pts = [];
  for (let r = 0; r < G; r++)
    for (let c = 0; c < G; c++)
      if (skel[r][c]) pts.push({ r, c, d: 1 });

  const path = orderPixels(pts, G, cell);
  const smoothed = smoothPath(path, 4);
  const bw = x2 - x1, bh = y2 - y1;
  return {
    path: smoothed.map(p => ({
      x: bw > 0 ? (p.x - x1) / bw : 0.5,
      y: bh > 0 ? (p.y - y1) / bh : 0.5,
    })),
    bounds: { x1, y1, x2, y2, w: bw, h: bh },
  };
}

function orderPixels(pixels, G, cell) {
  if (pixels.length === 0) return [];

  // Find topmost-leftmost as start
  let startIdx = 0;
  for (let i = 1; i < pixels.length; i++) {
    if (pixels[i].r < pixels[startIdx].r ||
      (pixels[i].r === pixels[startIdx].r && pixels[i].c < pixels[startIdx].c))
      startIdx = i;
  }

  // Nearest-neighbor chain
  const used = new Set();
  const ordered = [];
  let cur = startIdx;
  used.add(cur);
  ordered.push(pixels[cur]);

  while (ordered.length < pixels.length) {
    let bestDist = Infinity, bestIdx = -1;
    const cr = pixels[cur].r, cc = pixels[cur].c;
    for (let i = 0; i < pixels.length; i++) {
      if (used.has(i)) continue;
      const dr = pixels[i].r - cr, dc = pixels[i].c - cc;
      const d = dr * dr + dc * dc;
      if (d < bestDist) { bestDist = d; bestIdx = i; }
    }
    if (bestIdx === -1 || bestDist > 100) break; // gap too large, stop
    used.add(bestIdx);
    ordered.push(pixels[bestIdx]);
    cur = bestIdx;
  }

  // Convert to pixel coords
  return ordered.map(p => ({ x: (p.c + 0.5) * cell, y: (p.r + 0.5) * cell }));
}

function smoothPath(pts, windowSize) {
  if (pts.length < 3) return pts;
  const result = [];
  // Subsample first (take every Nth point)
  const step = Math.max(1, Math.floor(pts.length / 40));
  const sampled = [];
  for (let i = 0; i < pts.length; i += step) sampled.push(pts[i]);
  if (sampled.length < pts.length && sampled[sampled.length - 1] !== pts[pts.length - 1])
    sampled.push(pts[pts.length - 1]);

  // Moving average smooth
  for (let i = 0; i < sampled.length; i++) {
    let sx = 0, sy = 0, cnt = 0;
    for (let j = Math.max(0, i - windowSize); j <= Math.min(sampled.length - 1, i + windowSize); j++) {
      sx += sampled[j].x; sy += sampled[j].y; cnt++;
    }
    result.push({ x: sx / cnt, y: sy / cnt });
  }
  return result;
}

