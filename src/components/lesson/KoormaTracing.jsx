function TraceStep({ vowel, onComplete, size }) {
  const drawRef = useRef(null), guideRef = useRef(null), maskRef = useRef(null);
  const allPts = useRef([]), curPts = useRef([]);
  const [drawing, setDrawing] = useState(false);
  const [hasDrawn, setHasDrawn] = useState(false);
  const [state, setState] = useState("ready");
  const [attempts, setAttempts] = useState(0);
  const [hint, setHint] = useState(false);
  const [result, setResult] = useState(null);
  const S = size ?? 300;

  useEffect(() => {
    const t = setTimeout(() => {
      const m = analyzeLetter(vowel, S);
      maskRef.current = m;
      iCv(drawRef, S); iCv(guideRef, S);
      paintGuide(guideRef, vowel, m, false, S);
    }, 200);
    return () => clearTimeout(t);
  }, [vowel]);

  useEffect(() => {
    if (maskRef.current) paintGuide(guideRef, vowel, maskRef.current, hint, S);
  }, [hint, vowel]);

  const pos = useCallback((e) => cPos(e, drawRef, S), []);
  const onStart = useCallback((e) => {
    e.preventDefault(); if (state === "success") return;
    setDrawing(true); setHasDrawn(true);
    if (state !== "tracing") setState("tracing");
    const p = pos(e); curPts.current = [p];
    const ctx = rCtx(drawRef, S);
    if (ctx) { ink(ctx, C.terra); ctx.beginPath(); ctx.moveTo(p.x, p.y); }
  }, [pos, state]);
  const onMove = useCallback((e) => {
    e.preventDefault(); if (!drawing) return;
    const p = pos(e); curPts.current.push(p);
    const ctx = rCtx(drawRef, S);
    if (ctx) { ink(ctx, C.terra); ctx.lineTo(p.x, p.y); ctx.stroke(); ctx.beginPath(); ctx.moveTo(p.x, p.y); }
  }, [drawing, pos]);
  const onEnd = useCallback(() => {
    if (!drawing) return; setDrawing(false);
    allPts.current = [...allPts.current, ...curPts.current]; curPts.current = [];
  }, [drawing]);
  const check = () => {
    if (!maskRef.current) return;
    const r = validate(allPts.current, maskRef.current, { minOn: 0.50, minZ: 0.70, minBB: 0.55 });
    setResult(r);
    const n = attempts + 1; setAttempts(n);
    if (r.pass) setState("success"); else { setState("failed"); if (n >= 2) setHint(true); }
  };
  const clear = () => {
    clr(drawRef); allPts.current = []; curPts.current = [];
    setHasDrawn(false); setState("ready"); setResult(null);
  };

  return (
    <Col>
      <Hdr title="Trace the Letter ✏️"
        sub={<>Follow the dashed path on <Te>{vowel.letter}</Te> ({vowel.trans})</>} />
      <Box size={S} state={state}>
        <canvas ref={guideRef} style={{ position: "absolute", inset: 0, pointerEvents: "none" }} />
        <canvas ref={drawRef} style={{ position: "absolute", inset: 0, touchAction: "none", cursor: "crosshair" }}
          onMouseDown={onStart} onMouseMove={onMove} onMouseUp={onEnd} onMouseLeave={onEnd}
          onTouchStart={onStart} onTouchMove={onMove} onTouchEnd={onEnd} />
        {state === "ready" && !hasDrawn && <Pill>Start from ① and follow the arrows!</Pill>}
      </Box>
      {state === "success" && <FB icon="✨" text={result?.fb} color={C.mango}
        detail={result?.zc > 0.85 ? "⭐⭐⭐ అద్భుతం! (Wonderful!)" : "⭐⭐ బాగుంది! (Good job!)"} />}
      {state === "failed" && result && <FB icon="🐢" text={result.fb} color={C.terra}
        detail={result.missed <= 2 ? "⭐ Almost! Try again!" : "Keep tracing the whole letter! 🐢"} />}
      <BtnRow>
        {state === "success" ? <Btn onClick={onComplete} bg={C.mango} full>Next Step →</Btn>
          : state === "failed" ? <>
            <BtnO onClick={clear} full>🔄 Try again</BtnO>
            {attempts >= 4 && <Btn onClick={onComplete} bg={C.darkMuted} full>Continue →</Btn>}
          </> : <>
            <BtnO onClick={clear} full>🔄 Clear</BtnO>
            <Btn onClick={check} bg={C.turmeric} full disabled={!hasDrawn}>Check ✓</Btn>
          </>}
      </BtnRow>
    </Col>
  );
}

