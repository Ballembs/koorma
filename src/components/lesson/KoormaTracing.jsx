function iCv(ref, s) {
  const c = ref.current; if (!c) return; const ctx = c.getContext("2d"); const d = window.devicePixelRatio || 1;
  c.width = s * d; c.height = s * d; ctx.scale(d, d); c.style.width = s + "px"; c.style.height = s + "px";
}
function rCtx(ref, s) {
  const c = ref.current; if (!c) return null; const ctx = c.getContext("2d"); const d = window.devicePixelRatio || 1;
  ctx.setTransform(1, 0, 0, 1, 0, 0); ctx.scale(d, d); return ctx;
}
function ink(ctx, color) { ctx.strokeStyle = color; ctx.lineWidth = 6; ctx.lineCap = "round"; ctx.lineJoin = "round"; }
function clr(ref) { const c = ref.current; if (c) c.getContext("2d").clearRect(0, 0, c.width, c.height); }
function cPos(e, ref, s) {
  const c = ref.current; if (!c) return { x: 0, y: 0 }; const r = c.getBoundingClientRect();
  const sx = s / r.width, sy = s / r.height;
  if (e.touches?.[0]) return { x: (e.touches[0].clientX - r.left) * sx, y: (e.touches[0].clientY - r.top) * sy };
  return { x: (e.clientX - r.left) * sx, y: (e.clientY - r.top) * sy };
}
function p100(v) { return Math.round((v || 0) * 100) + "%"; }

