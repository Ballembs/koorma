function paintGuide(ref, vowel, mask, hint, S) {
  const ctx = rCtx(ref, S); if (!ctx) return;
  ctx.clearRect(0, 0, S, S);

  // 1. Filled letter background
  ctx.font = `800 ${mask.fs}px "Noto Sans Telugu", sans-serif`;
  ctx.textAlign = "center"; ctx.textBaseline = "middle";
  ctx.fillStyle = hint ? "rgba(212,148,12,0.22)" : "rgba(185,170,148,0.20)";
  ctx.fillText(vowel.letter, S / 2, S / 2);

  const strokes = mask.guideStrokes;
  if (!strokes) return;

  // 2. Dashed centerline through waypoints
  for (const stroke of strokes) {
    if (stroke.length < 2) continue;
    ctx.save();
    ctx.strokeStyle = hint ? "rgba(212,148,12,0.55)" : "rgba(140,120,90,0.45)";
    ctx.lineWidth = 2.5;
    ctx.setLineDash([5, 5]);
    ctx.lineCap = "round"; ctx.lineJoin = "round";
    ctx.beginPath();
    ctx.moveTo(stroke[0].x, stroke[0].y);

    // Smooth curve through points
    for (let i = 1; i < stroke.length - 1; i++) {
      const mx = (stroke[i].x + stroke[i + 1].x) / 2;
      const my = (stroke[i].y + stroke[i + 1].y) / 2;
      ctx.quadraticCurveTo(stroke[i].x, stroke[i].y, mx, my);
    }
    ctx.lineTo(stroke[stroke.length - 1].x, stroke[stroke.length - 1].y);
    ctx.stroke();
    ctx.restore();

    // 3. Direction arrows
    const interval = Math.max(3, Math.floor(stroke.length / 5));
    for (let i = interval; i < stroke.length - 1; i += interval) {
      const prev = stroke[Math.max(0, i - 1)];
      const next = stroke[Math.min(stroke.length - 1, i + 1)];
      const dx = next.x - prev.x, dy = next.y - prev.y;
      const len = Math.sqrt(dx * dx + dy * dy);
      if (len < 2) continue;
      ctx.save();
      ctx.translate(stroke[i].x, stroke[i].y);
      ctx.rotate(Math.atan2(dy, dx));
      ctx.fillStyle = hint ? "rgba(212,148,12,0.65)" : "rgba(140,120,90,0.55)";
      ctx.beginPath();
      ctx.moveTo(7, 0); ctx.lineTo(-4, -4.5); ctx.lineTo(-4, 4.5);
      ctx.closePath(); ctx.fill();
      ctx.restore();
    }
  }

  // 4. Start dots
  strokes.forEach((stroke, si) => {
    if (stroke.length === 0) return;
    const sp = stroke[0];
    ctx.beginPath(); ctx.arc(sp.x, sp.y, 15, 0, Math.PI * 2);
    ctx.fillStyle = "rgba(76,175,80,0.12)"; ctx.fill();
    ctx.beginPath(); ctx.arc(sp.x, sp.y, 11, 0, Math.PI * 2);
    ctx.fillStyle = C.success; ctx.fill();
    ctx.strokeStyle = "white"; ctx.lineWidth = 2; ctx.stroke();
    ctx.fillStyle = "white";
    ctx.font = "bold 12px 'Nunito', sans-serif";
    ctx.textAlign = "center"; ctx.textBaseline = "middle";
    ctx.fillText(String(si + 1), sp.x, sp.y + 0.5);

    if (stroke.length >= 2) {
      const nx = stroke[1];
      const dx = nx.x - sp.x, dy = nx.y - sp.y;
      const len = Math.sqrt(dx * dx + dy * dy);
      if (len > 5) {
        const ux = dx / len, uy = dy / len;
        const ax = sp.x + ux * 22, ay = sp.y + uy * 22;
        ctx.beginPath();
        ctx.strokeStyle = C.success; ctx.lineWidth = 2.5; ctx.setLineDash([]);
        ctx.moveTo(sp.x + ux * 13, sp.y + uy * 13);
        ctx.lineTo(ax, ay); ctx.stroke();
        ctx.save();
        ctx.translate(ax, ay); ctx.rotate(Math.atan2(dy, dx));
        ctx.fillStyle = C.success;
        ctx.beginPath();
        ctx.moveTo(6, 0); ctx.lineTo(-4, -4); ctx.lineTo(-4, 4);
        ctx.closePath(); ctx.fill();
        ctx.restore();
      }
    }
  });
}

